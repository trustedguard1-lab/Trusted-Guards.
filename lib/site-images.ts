export function getSiteImageUrl(path: string) {
  const normalizedPath = path.toLowerCase()
  const exactMatch = exactImageMap[normalizedPath]

  if (exactMatch) {
    return exactMatch
  }

  const fallback = keywordFallbacks.find(({ keywords }) => keywords.some((keyword) => normalizedPath.includes(keyword)))
  return fallback?.url || operationsImage
}